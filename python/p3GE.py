from turtle import Turtle
turtle=Turtle()
def Heart():
	turtle.begin_fill()
	turtle.left(45)
	turtle.fd(100)
	turtle.left(90)
	turtle.circle(50,180)
	turtle.left(90)
	turtle.circle(50,180)
	turtle.left(90)
	turtle.forward(100)
	turtle.end_fill()
def Flower():
	turtle.color("lightgreen","lightgreen")
	turtle.left(90)#stem
	turtle.fd(20)
	turtle.right(90)
	turtle.fd(30)
	turtle.begin_fill()#leaf one
	turtle.circle(30)
	turtle.end_fill()
	turtle.left(180)
	turtle.fd(45)
	turtle.right(180)
	turtle.color("green","green")
	turtle.begin_fill()#leaf 2
	turtle.circle(30)
	turtle.end_fill()
	turtle.fd(15)#rest of stem
	turtle.left(90)
	turtle.fd(100)
	turtle.begin_fill()
	turtle.color("yellow","red")#petals
	turtle.fd(10)
	turtle.circle(30)
	turtle.left(45)
	turtle.circle(30)
	turtle.left(45)
	turtle.circle(30)
	turtle.left(45)
	turtle.circle(30)
	turtle.left(45)
	turtle.circle(30)
	turtle.left(45)
	turtle.circle(30)
	turtle.left(45)
	turtle.circle(30)
	turtle.left(45)
	turtle.circle(30)
	turtle.left(45)
	turtle.end_fill()
def Background():
	turtle.begin_fill()
	turtle.color("darkgreen","darkgreen")#grass
	turtle.fd(200)
	turtle.right(90)
	turtle.fd(100)
	turtle.right(90)
	turtle.fd(400)
	turtle.right(90)
	turtle.fd(100)
	turtle.right(90)
	turtle.fd(400)
	turtle.end_fill()
	turtle.begin_fill()
	turtle.color("blue","blue")#sky
	turtle.left(90)
	turtle.fd(200)
	turtle.left(90)
	turtle.fd(400)
	turtle.left(90)
	turtle.fd(200)
	turtle.end_fill()
turtle.home()
Background()
turtle.left(90)
turtle.fd(100)
turtle.color("red","red")
Heart()
turtle.penup()
turtle.home()
turtle.fd(100)
turtle.pendown()
turtle.color("pink","pink")
Heart()
turtle.penup()
turtle.home()
turtle.pendown()
Flower()
turtle.exitonclick()

